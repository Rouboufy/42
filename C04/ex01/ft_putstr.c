#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(void)
{
	ft_putstr("123456789098765432123455677890987654321234567890987654321\n");
	ft_putstr("42 piscine\n");
	return (0);
}
